//Math operator 
//Ab iske baare mai baat karte hain, math operators ka use hum numbers ke saath karte hain, jaise addition, subtraction, multiplication, division, modulus, increment and decrement.
//ye sab jageh tang karne ata hai par padna toh padega 

//Math.max(x,y) method can be used to find the highest value of x and y
Math.max(5, 10); // returns 10

//Math.min(x,y) method can be used to find the lowest value of x and y
Math.min(5, 10); // returns 5

//Math.sqrt(x) method returns the square root of x
Math.sqrt(16); // returns 4.0

//Math.abs(x) method returns the absolute (positive) value of x
Math.abs(-5); // returns 5

//Math.pow(x, y) method returns the value of x raised to the power of y
Math.pow(2, 3); // returns 8.0 (2 raised to the power of 3 is 8)

//Math.round(x) - rounds to the nearest integer
Math.round(4.5); // returns 5

//Math.ceil(x) - rounds up (returns the smallest integer greater than or equal to x)
Math.ceil(4.2); // returns 5.0

//Math.floor(x) - rounds down (returns the largest integer less than or equal to x)
Math.floor(4.8); // returns 4.0

//Math.random() returns a random number between 0.0 (inclusive), and 1.0 (exclusive)
double randomNum = Math.random();
System.out.println(randomNum); // Outputs a random number between 0.0 and 1.0


